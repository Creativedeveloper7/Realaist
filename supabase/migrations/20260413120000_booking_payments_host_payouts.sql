-- ---------------------------------------------------------------------------
-- Validate scheduled_visit.booking_payment_id matches success + property
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION validate_scheduled_visit_booking_payment()
RETURNS TRIGGER AS $$
BEGIN
  IF NEW.booking_payment_id IS NOT NULL THEN
    IF NOT EXISTS (
      SELECT 1 FROM booking_payments bp
      WHERE bp.id = NEW.booking_payment_id
        AND bp.status = 'success'
        AND bp.property_id = NEW.property_id
    ) THEN
      RAISE EXCEPTION 'booking_payment_id must reference a successful booking_payment for this property';
    END IF;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_scheduled_visits_booking_payment ON scheduled_visits;
CREATE TRIGGER trg_scheduled_visits_booking_payment
  BEFORE INSERT OR UPDATE OF booking_payment_id, property_id ON scheduled_visits
  FOR EACH ROW EXECUTE FUNCTION validate_scheduled_visit_booking_payment();

-- ---------------------------------------------------------------------------
-- Link booking payment row to visit after insert (reference must match)
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.link_booking_payment_to_visit(
  p_payment_id UUID,
  p_visit_id UUID,
  p_reference TEXT
)
RETURNS BOOLEAN
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  updated_count INT;
BEGIN
  UPDATE booking_payments bp
  SET scheduled_visit_id = p_visit_id
  WHERE bp.id = p_payment_id
    AND bp.paystack_reference = p_reference
    AND bp.status = 'success'
    AND bp.scheduled_visit_id IS NULL;

  GET DIAGNOSTICS updated_count = ROW_COUNT;
  RETURN updated_count > 0;
END;
$$;

REVOKE ALL ON FUNCTION public.link_booking_payment_to_visit(UUID, UUID, TEXT) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.link_booking_payment_to_visit(UUID, UUID, TEXT) TO anon;
GRANT EXECUTE ON FUNCTION public.link_booking_payment_to_visit(UUID, UUID, TEXT) TO authenticated;

