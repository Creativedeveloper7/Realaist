-- Booking payments (short-stay guest charges) — separate from campaign `payments` table.
-- Host M-Pesa payout profile + Paystack transfer ledger.

-- ---------------------------------------------------------------------------
-- booking_payments
-- ---------------------------------------------------------------------------
DO $$ BEGIN
  CREATE TYPE booking_payment_status AS ENUM (
    'pending',
    'processing',
    'success',
    'failed',
    'refunded'
  );
EXCEPTION
  WHEN duplicate_object THEN NULL;
END $$;

CREATE TABLE IF NOT EXISTS booking_payments (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  property_id UUID NOT NULL REFERENCES properties(id) ON DELETE CASCADE,
  host_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  payer_user_id UUID REFERENCES auth.users(id) ON DELETE SET NULL,
  scheduled_visit_id UUID REFERENCES scheduled_visits(id) ON DELETE SET NULL,

  paystack_reference TEXT NOT NULL,
  amount_requested_minor BIGINT NOT NULL,
  amount_paid_minor BIGINT,
  platform_fee_minor BIGINT NOT NULL DEFAULT 0,
  host_net_minor BIGINT NOT NULL,
  currency TEXT NOT NULL DEFAULT 'KES',

  status booking_payment_status NOT NULL DEFAULT 'pending',

  customer_email TEXT NOT NULL,
  customer_name TEXT,
  metadata JSONB NOT NULL DEFAULT '{}',

  paid_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),

  CONSTRAINT booking_payments_paystack_reference_unique UNIQUE (paystack_reference),
  CONSTRAINT booking_payments_amounts_non_negative CHECK (
    amount_requested_minor >= 0
    AND (amount_paid_minor IS NULL OR amount_paid_minor >= 0)
    AND platform_fee_minor >= 0
    AND host_net_minor >= 0
  )
);

CREATE INDEX IF NOT EXISTS idx_booking_payments_property_id ON booking_payments(property_id);
CREATE INDEX IF NOT EXISTS idx_booking_payments_host_id ON booking_payments(host_id);
CREATE INDEX IF NOT EXISTS idx_booking_payments_status ON booking_payments(status);
CREATE INDEX IF NOT EXISTS idx_booking_payments_payer ON booking_payments(payer_user_id);
CREATE INDEX IF NOT EXISTS idx_booking_payments_scheduled_visit ON booking_payments(scheduled_visit_id);

-- ---------------------------------------------------------------------------
-- scheduled_visits: link to booking payment (nullable for legacy rows)
-- ---------------------------------------------------------------------------
ALTER TABLE scheduled_visits
  ADD COLUMN IF NOT EXISTS booking_payment_id UUID REFERENCES booking_payments(id) ON DELETE SET NULL;

CREATE INDEX IF NOT EXISTS idx_scheduled_visits_booking_payment_id ON scheduled_visits(booking_payment_id);

-- ---------------------------------------------------------------------------
-- host_payment_profiles — M-Pesa / Paystack recipient (Kenya)
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS host_payment_profiles (
  host_id UUID PRIMARY KEY REFERENCES profiles(id) ON DELETE CASCADE,
  mpesa_phone TEXT NOT NULL DEFAULT '',
  account_holder_name TEXT,
  -- Paystack mobile_money: provider code from GET /bank?currency=KES&type=mobile_money (e.g. Safaricom)
  mpesa_provider_code TEXT,
  paystack_recipient_code TEXT,
  payout_currency TEXT NOT NULL DEFAULT 'KES',
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- ---------------------------------------------------------------------------
-- host_transfers — withdrawals to host M-Pesa (Paystack Transfer API)
-- transfer_reference: UUID v4 generated by Edge Function (Paystack-compatible)
-- ---------------------------------------------------------------------------
DO $$ BEGIN
  CREATE TYPE host_transfer_status AS ENUM (
    'pending',
    'queued',
    'processing',
    'success',
    'failed',
    'rejected',
    'otp_required'
  );
EXCEPTION
  WHEN duplicate_object THEN NULL;
END $$;

CREATE TABLE IF NOT EXISTS host_transfers (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  host_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  amount_minor BIGINT NOT NULL,
  currency TEXT NOT NULL DEFAULT 'KES',
  transfer_reference TEXT NOT NULL,
  paystack_transfer_code TEXT,
  paystack_transfer_numeric_id BIGINT,
  recipient_code_snapshot TEXT,
  status host_transfer_status NOT NULL DEFAULT 'pending',
  reason TEXT,
  failure_reason TEXT,
  metadata JSONB NOT NULL DEFAULT '{}',
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),

  CONSTRAINT host_transfers_reference_unique UNIQUE (transfer_reference),
  CONSTRAINT host_transfers_amount_positive CHECK (amount_minor > 0)
);

CREATE INDEX IF NOT EXISTS idx_host_transfers_host_id ON host_transfers(host_id);
CREATE INDEX IF NOT EXISTS idx_host_transfers_status ON host_transfers(status);
CREATE INDEX IF NOT EXISTS idx_host_transfers_created ON host_transfers(created_at DESC);

